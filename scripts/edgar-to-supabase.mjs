const sleep = (ms) => new Promise(r => setTimeout(r, ms));

async function fetchSEC(url) {
  await sleep(REQUEST_DELAY);
  const res = await fetch(url, {
    headers: { 'User-Agent': USER_AGENT, 'Accept': 'application/json, text/xml, text/html, */*' },
  });
  if (!res.ok) throw new Error(`SEC ${res.status}: ${url}`);
  const ct = res.headers.get('content-type') || '';
  if (ct.includes('json')) return await res.json();
  return await res.text();
}

function extractTag(text, tag) {
  const m = text.match(new RegExp(`<(?:[\\w]+:)?${tag}[^>]*>([^<]*)`, 'i'));
  return m ? decodeHTMLEntities(m[1]) : null;
}

// HTML 엔티티 디코딩 (&amp; → &, &lt; → <, 등)
function decodeHTMLEntities(str) {
  if (!str) return str;
  return str
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&apos;/g, "'");
}

