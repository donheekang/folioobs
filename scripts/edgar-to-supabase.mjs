// XML 파싱 + DB Insert
// ============================================================
async function processXML(xmlData, inv, investorId, accession, filingDate, reportDate, quarter, xmlUrl, xmlFile, prevIdx, reportDates, dates, accessions) {
  // 파싱
  const entries = xmlData.split(/<\/(?:[\w]+:)?infoTable>/i);
  const holdings = [];
  for (const entry of entries) {
    const name = extractTag(entry, 'nameOfIssuer');
    const cusip = extractTag(entry, 'cusip');
    const val = extractTag(entry, 'value');
    const shares = extractTag(entry, 'sshPrnamt') || extractTag(entry, 'sshPrnAmt');
    const putCall = extractTag(entry, 'putCall');
    if (!cusip || !val) continue;
    holdings.push({
      cusip: cusip.trim(),
      name: (name || 'Unknown').trim(),
      value: parseInt(val.replace(/,/g, ''), 10),
      shares: parseInt((shares || '0').replace(/,/g, ''), 10),
      optionType: putCall?.trim() || null,
    });
  }

  if (holdings.length === 0) {
    console.log('  ⚠️ 파싱된 종목 없음');
    return;
  }

  console.log(`  ${holdings.length}개 종목 파싱 완료`);
  const totalValue = holdings.reduce((s, h) => s + h.value, 0);

  // Filing 등록
  const { data: newFiling, error: filingErr } = await supabase
    .from('filings')
    .insert({
      investor_id: investorId,
      accession_no: accession,
      filing_date: filingDate,
      report_date: reportDate,
      quarter,
      total_value: totalValue,
      holding_count: holdings.length,
      xml_url: xmlUrl,
      parsed_at: new Date().toISOString(),
    })
    .select('id')
    .single();

  if (filingErr) {
    console.error(`  ❌ 파일링 등록 실패: ${filingErr.message}`);
    return;
  }

  // Securities upsert + Holdings insert
  let insertCount = 0;
  const batchSize = 20;
  console.log(`  티커 매핑 + DB 저장 중...`);

  for (let i = 0; i < holdings.length; i += batchSize) {
    const batch = holdings.slice(i, i + batchSize);

    for (const h of batch) {
      const tickerInfo = await resolveTicker(h.cusip, h.name);
      const sector = classifySector(h.name);

      // Security upsert
      const { data: sec } = await supabase
        .from('securities')
        .upsert({
          cusip: h.cusip,
          ticker: tickerInfo.ticker,
          name: h.name,
          name_ko: h.name,
          sector,
          sector_ko: sector,
        }, { onConflict: 'cusip' })
        .select('id')
        .single();

      if (!sec) continue;

      // Holding insert
      const { error: holdErr } = await supabase
        .from('holdings')
        .insert({
          filing_id: newFiling.id,
          investor_id: investorId,
          security_id: sec.id,
          quarter,
          shares: h.shares,
          value: h.value,
          pct_of_portfolio: totalValue > 0 ? Math.round(h.value / totalValue * 10000) / 100 : 0,
          option_type: h.optionType,
        });

      if (!holdErr) insertCount++;
    }

    // 진행 상황
    const progress = Math.min(i + batchSize, holdings.length);
    process.stdout.write(`\r  진행: ${progress}/${holdings.length}`);
  }
  console.log(`\n  ✅ DB 등록: ${insertCount}/${holdings.length}개 종목`);

  // holding_changes는 main()에서 일괄 계산 (모든 분기 처리 후)

  // investor_metrics 업데이트
  const sectors = new Set(holdings.map(h => classifySector(h.name)));
  const sorted = [...holdings].sort((a, b) => b.value - a.value);
  const top10Pct = sorted.slice(0, 10).reduce((s, h) => s + (totalValue > 0 ? h.value / totalValue : 0), 0);

  await supabase.from('investor_metrics').upsert({
    investor_id: investorId,
    quarter,
    total_aum: totalValue,
    holding_count: holdings.length,
    sector_count: sectors.size,
    concentration: Math.round(top10Pct * 1000) / 1000,
    top_holding_pct: totalValue > 0 ? Math.round(sorted[0].value / totalValue * 10000) / 100 : 0,
    qoq_change: 0,
  }, { onConflict: 'investor_id,quarter' });

  console.log(`  📈 메트릭 저장: AUM $${(totalValue / 1000000).toFixed(1)}B / ${holdings.length}종목 / ${sectors.size}섹터`);
}

main().catch(err => { console.error('실패:', err); process.exit(1); });
