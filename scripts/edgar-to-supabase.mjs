async function resolveTicker(cusip, name) {
  // 1) 수동 매핑 우선
  if (CUSIP_TICKER_OVERRIDES[cusip]) {
    const override = { ticker: CUSIP_TICKER_OVERRIDES[cusip], name: cusipCache[cusip]?.name || name };
    cusipCache[cusip] = override;
    return override;
  }
  // 2) 캐시에 있고 티커가 유효하면 (숫자로 시작하지 않으면) 반환
  if (cusipCache[cusip]) {
    const cached = cusipCache[cusip];
    if (cached.ticker && !/^\d/.test(cached.ticker)) return cached;
    // 캐시된 티커가 깨져있으면 다시 시도
  }
  // 3) OpenFIGI API
  try {
    await sleep(120);
    const res = await fetch('https://api.openfigi.com/v3/mapping', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify([{ idType: 'ID_CUSIP', idValue: cusip }]),
    });
    if (res.ok) {
      const d = await res.json();
      if (d[0]?.data?.[0]?.ticker) {
        cusipCache[cusip] = { ticker: d[0].data[0].ticker, name: d[0].data[0].name || name };
        return cusipCache[cusip];
      }
    }
  } catch (e) { /* */ }
  // 4) Fallback: CUSIP 앞 6자리 대신 회사명 약어 생성
  const abbr = name.replace(/\s*(INC|CORP|LTD|PLC|CO|GROUP|HOLDINGS?|CLASS\s*[A-Z]|CL\s*[A-Z]|SHS|DEL|NEW|COM)\s*/gi, '')
    .trim().split(/\s+/).map(w => w[0]).join('').toUpperCase().slice(0, 5) || cusip.slice(0, 6);
  cusipCache[cusip] = { ticker: abbr, name };
  return cusipCache[cusip];
}

