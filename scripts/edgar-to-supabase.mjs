async function fetch13FInfoTable(accession, cik, primaryDoc) {
  const accClean = accession.replace(/-/g, '');
  const accDashed = accession; // 예: 0001193125-26-054580
  const cikClean = cik.replace(/^0+/, '');
  const baseUrl = `https://www.sec.gov/Archives/edgar/data/${cikClean}/${accClean}`;

  // 방법 1: index.json에서 파일 목록 가져와서 infotable 찾기
  console.log(`  파일 목록 조회 중...`);
  try {
    const indexData = await fetchSEC(`${baseUrl}/index.json`);
    const items = indexData?.directory?.item || [];
    const names = items.map(f => f.name || '');

    console.log(`  파일 ${names.length}개: ${names.filter(n => n.endsWith('.xml')).join(', ')}`);

    // infotable XML 찾기 (다양한 이름 패턴)
    const infoFile = names.find(n => /infotable/i.test(n) && /\.xml$/i.test(n))
      || names.find(n => /information.*table/i.test(n) && /\.xml$/i.test(n))
      || names.find(n => /13[fF].*\.xml$/i.test(n) && !/primary/i.test(n) && !/form13f.*\.htm/i.test(n) && !/xsl/i.test(n));

    if (infoFile) {
      console.log(`  XML 발견: ${infoFile}`);
      const xmlData = await fetchSEC(`${baseUrl}/${infoFile}`);
      return { xmlData, xmlUrl: `${baseUrl}/${infoFile}` };
    }
  } catch (e) {
    console.log(`  index.json 실패: ${e.message}`);
  }

  // 방법 2: 일반적인 파일명 직접 시도
  const commonNames = [
    'infotable.xml',
    'InfoTable.xml',
    'INFOTABLE.XML',
    'form13fInfoTable.xml',
    `${accDashed}-infotable.xml`,
    'information_table.xml',
  ];

  for (const name of commonNames) {
    try {
      const xmlData = await fetchSEC(`${baseUrl}/${name}`);
      console.log(`  XML 발견 (직접): ${name}`);
      return { xmlData, xmlUrl: `${baseUrl}/${name}` };
    } catch (e) {
      // 다음 시도
    }
  }

  // 방법 3: EDGAR filing-documents API (data.sec.gov)
  try {
    console.log(`  EDGAR documents API 시도...`);
    const docsUrl = `https://data.sec.gov/submissions/CIK${cik}.json`;
    // submissions에서 이미 가져왔으니 여기서는 direct document fetch 시도
  } catch (e) { /* */ }

  // 방법 4: primary_doc.xml 내에서 데이터 추출 시도 (XSL 변환된 형태)
  if (primaryDoc) {
    try {
      console.log(`  primary doc 파싱 시도: ${primaryDoc}`);
      const xmlData = await fetchSEC(`${baseUrl}/${primaryDoc}`);
      // primary_doc이 실제 infotable 데이터를 포함하는지 확인
      if (typeof xmlData === 'string' && (xmlData.includes('nameOfIssuer') || xmlData.includes('NAMEOFISSUER'))) {
        return { xmlData, xmlUrl: `${baseUrl}/${primaryDoc}` };
      }
      console.log(`  primary doc에 holdings 데이터 없음`);
    } catch (e) { /* */ }
  }

  // 방법 5: HTML 인덱스 페이지에서 직접 링크 추출
  try {
    console.log(`  HTML 인덱스 페이지 스캔...`);
    const html = await fetchSEC(`${baseUrl}/`);
    if (typeof html === 'string') {
      // 모든 XML 링크 추출
      const allLinks = [];
      const regex = /href="([^"]*\.xml[^"]*)"/gi;
      let m;
      while ((m = regex.exec(html)) !== null) {
        allLinks.push(m[1]);
      }

      // 1차: infotable 이름 매칭
      for (const link of allLinks) {
        const fileName = link.split('/').pop();
        if (/infotable|information.*table/i.test(fileName)) {
          const fullUrl = link.startsWith('http') ? link
            : link.startsWith('/') ? `https://www.sec.gov${link}`
            : `${baseUrl}/${link}`;
          console.log(`  HTML에서 발견: ${fileName}`);
          const xmlData = await fetchSEC(fullUrl);
          return { xmlData, xmlUrl: fullUrl };
        }
      }

      // 2차: primary_doc 제외한 모든 XML 파일을 시도하고 holdings 데이터 포함 여부 확인
      const candidates = allLinks.filter(l => {
        const fn = l.split('/').pop().toLowerCase();
        return fn.endsWith('.xml') && !fn.includes('primary_doc') && !fn.includes('xsl');
      });

      console.log(`  XML 후보 ${candidates.length}개: ${candidates.map(l => l.split('/').pop()).join(', ')}`);

      for (const link of candidates) {
        const fullUrl = link.startsWith('http') ? link
          : link.startsWith('/') ? `https://www.sec.gov${link}`
          : `${baseUrl}/${link}`;
        try {
          const xmlData = await fetchSEC(fullUrl);
          // nameOfIssuer 또는 cusip 태그가 있으면 infotable 데이터
          if (typeof xmlData === 'string' &&
              (xmlData.includes('nameOfIssuer') || xmlData.includes('NAMEOFISSUER') ||
               xmlData.includes('nameofissuer'))) {
            console.log(`  ✅ holdings 데이터 발견: ${link.split('/').pop()}`);
            return { xmlData, xmlUrl: fullUrl };
          }
        } catch (e) { /* 다음 시도 */ }
      }
    }
  } catch (e) {
    console.log(`  HTML 스캔 실패: ${e.message}`);
  }

  return null;
}

