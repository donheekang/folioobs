function classifySector(name) {
  // 1) HTML 엔티티 디코딩 (&amp; → &, etc.)
  let decoded = (name || '')
    .replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"').replace(/&#39;/g, "'").replace(/&apos;/g, "'");
  const raw = decoded.toUpperCase();
  // 2) 하이픈 제거 버전도 함께 비교 (COCA-COLA → COCA COLA)
  const noHyphen = raw.replace(/-/g, ' ').replace(/\s+/g, ' ');
  // 3) SEC 축약어를 풀네임으로 확장한 버전 생성
  let expanded = ' ' + noHyphen + ' ';
  for (const [pat, repl] of SEC_ABBREVS) {
    expanded = expanded.replace(pat, repl);
  }
  expanded = expanded.trim();

  for (const [sector, kws] of Object.entries(SECTOR_KEYWORDS)) {
    if (kws.some(kw =>
      raw.includes(kw) ||
      noHyphen.includes(kw.replace(/-/g, ' ')) ||
      expanded.includes(kw)
    )) return sector;
  }
  return '기타';
}

